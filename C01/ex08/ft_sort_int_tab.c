void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int k;

	i = 0;
	while (i < (size - 1))
	{
		k = 0;
		while (k < (size - i - 1))
		{
			if (tab[k] > tab[k + 1])
			{
				j = tab[k];
				tab[k] = tab[k + 1];
				tab[k + 1] = j;
			}
			k++;
		}
		i++;
	}
}
