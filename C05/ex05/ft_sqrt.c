int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (0);
	while ((i * i < nb) && (i < 46340))
		i++;
	if (nb % i == 0 && nb != 0 && nb != 2)
		return (i);
	return (0);
}
